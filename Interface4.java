    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        conn = DBConnection1.ConnectDB();
        
        try {

            if (jTextField2.getText().isEmpty() && jTextField3.getText().isEmpty() && jTextField4.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Please Enter Details!");
            } else if (jTextField3.getText().isEmpty() && jTextField4.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Please Enter Quantity and price!");
            } else if (jTextField2.getText().isEmpty() && jTextField4.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Please Enter Name and price!");
            } else if (jTextField2.getText().isEmpty() && jTextField3.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Please Enter Name and Quantity!");
            } else if (jTextField2.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Please Enter Name!");
            } else if (jTextField3.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Please Enter Quantity!");
            } else if (jTextField4.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Please Enter Price!");
            } else {
                String name = jTextField2.getText();
                String nm = null;
                String sql = "Select name from ITEMS where name='" + name + "'";
                pst = conn.prepareStatement(sql);
                rs = pst.executeQuery();
                while (rs.next()) {
                    nm = rs.getString("name");
                }
                if (name.equals(nm)) {
                    JOptionPane.showMessageDialog(null, " Item with this name already exists!");
                } else {
                    pst = conn.prepareStatement("insert into ITEMS(NAME,QUANTITY,PRICE) VALUES (?,?,?)");
                    pst.setString(1, jTextField2.getText());
                    pst.setInt(2, Integer.parseInt(jTextField3.getText()));
                    pst.setDouble(3, Double.parseDouble(jTextField4.getText()));
                    JOptionPane.showMessageDialog(null, "     Successfully saved!");
                    pst.executeUpdate();
                    pst.close();
                    DateFormat df = new SimpleDateFormat("MM/dd/yyyy HH:mm:ss");
                    Date date = new Date();
                    String DateTime = df.format(date);
                    String Name = jTextField2.getText();
                    int Quantity = Integer.parseInt(jTextField3.getText());
                    double Price = Double.parseDouble(jTextField4.getText());
                    Statement st = conn.createStatement();
                    st.executeUpdate("INSERT INTO Added(Date_Time,Name,Quantity,Price) VALUES ('" + DateTime + "','" + Name + "'," + Quantity + "," + Price + ")");
                    Interface4 obj9 = new Interface4();
                    obj9.setVisible(true);
                    this.dispose();
                }
            }
        } catch (NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Please enter valid values!");
        }
        catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Database Error!\nPlease contact system administrator!");
            Logger.getLogger(DBConnection1.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

