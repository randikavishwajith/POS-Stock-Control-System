    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            if ("".equals(jTextField1.getText())) {
                JOptionPane.showMessageDialog(null, "Please Enter Name");
            } else {
                File file = new File("Printer_name.txt");
                FileWriter fileWrite = new FileWriter(file);
                BufferedWriter bufferedWriter = new BufferedWriter(fileWrite);
                String printerName = jTextField1.getText();
                bufferedWriter.write(printerName);
                bufferedWriter.close();
                fileWrite.close();
                DocFlavor docFlavor = DocFlavor.BYTE_ARRAY.AUTOSENSE;
                PrintRequestAttributeSet pras = new HashPrintRequestAttributeSet();
                PrintService printService[] = PrintServiceLookup.lookupPrintServices(docFlavor, pras);
                String check = "false";
                for (PrintService service : printService) {
                    if (service.getName().equalsIgnoreCase(printerName)) {
                        check = "true";
                    }
                }
                if ("true".equals(check)) {
                    JOptionPane.showMessageDialog(null, "           Printer found!");
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "No printer found with that name!");
                }
            }
        } catch (IOException e) {
        }
    }//GEN-LAST:event_jButton1ActionPerformed

